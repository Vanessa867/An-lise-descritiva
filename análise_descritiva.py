plt.figure(figsize=(10, 6))
sns.boxplot(x='TipoEsc', y='Nota_Teste', data=df_interesse)
plt.title('Box Plot das Notas por Tipo de Escola')
plt.xlabel('Tipo de Escola')
plt.ylabel('Nota do Teste')
plt.show()

notas_escola_publica = df_interesse[df_interesse['TipoEsc'] == 'Pub']['Nota_Teste']
notas_escola_privada = df_interesse[df_interesse['TipoEsc'] == 'Priv']['Nota_Teste']

t_stat, p_value = ttest_ind(notas_escola_publica, notas_escola_privada)
print(f'Estatística t: {t_stat}, Valor p: {p_value}')

