import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from scipy.stats import ttest_ind

df = pd.read_csv('base_alunos.csv')

df_interesse = df[['Nota_Teste', 'TipoEsc']]

desc_stats = df_interesse.groupby('TipoEsc')['Nota_Teste'].describe()
print(desc_stats)

